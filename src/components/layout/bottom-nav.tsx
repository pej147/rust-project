export function BottomNav() {
  const pathname = usePathname();
  const { data: session } = useSession();

  // Add admin nav item if user is admin
  const items = isAdmin(session?.user?.role) ? [...navItems, adminNavItem] : navItems;

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-zinc-800 bg-zinc-950/80 backdrop-blur-xl">
      <div className="mx-auto flex max-w-lg items-center justify-around px-4 pb-6 pt-2">
        {items.map((item) => {
          const isActive = pathname.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex flex-col items-center gap-1 px-4 py-1 transition-colors",
                isActive ? "text-blue-500" : "text-zinc-400 hover:text-zinc-200"
              )}
            >
              {item.icon}
              <span className="text-xs font-medium">{item.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
}
